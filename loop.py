# Print numbers 0 through 4
for i in range(5):
    print(i)


# Count from 1 to 5
for i in range(1, 6):
    print(i)    

name = "Python"
for char in name:
    print(char)



colors = ["red", "blue", "green"]
for color in colors:
    print(color)    


#While loops
count = 0

while count < 5:
    print("Count:", count)
    count += 1
